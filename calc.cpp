// todo: calculator functionalities

template <typename _Ty>
_Ty add(_Ty a, _Ty b) {
    return a + b;
}

template <typename _Ty>
_Ty sub(_Ty a, _Ty b) {
    return a - b;
}

template <typename _Ty>
_Ty mul(_Ty a, _Ty b) {
    return a * b;
}

template <typename _Ty>
_Ty div(_Ty a, _Ty b) {
    return a / b;
}